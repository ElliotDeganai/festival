		<h3>Biographie :</h3>
	</div>
	<blockquote>
		<p>
			Dimitri Beg est le quatrième et dernier garçon d'une famille d'origine corse et provençale. Son père est un polytechnicien devenu officier de l'armée coloniale, ayant atteint le grade de général en fin de carrière et sa mère, femme au foyer, est une lectrice autodidacte1.  
		</p>
		<p>
			 Il passe son enfance au gré des garnisons paternelles, en Afrique (Djibouti, Éthiopie, Algérie, Afrique équatoriale), en Asie du Sud-Est (Indochine) et en France (notamment à La Colle-sur-Loup). C'est son père, féru de poésie, qui lui donnera très vite le goût des livres, qu'il dévore dans la bibliothèque familiale ou à l'école2.Il raconte dans Mon frère, paru en 2018, ses relations fortes avec son frère aîné Bernard Pennacchioni, disparu prématurément en 2003.
		</p>
